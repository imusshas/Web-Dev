// booleans: true & false

let num1 = 10;
let num2 = 20;

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= num2);
console.log(num1 <= num2);

let num3 = "15";
let num4 = 15;

console.log(num3 == num4); //true // checks value only. Doesn't check data type
console.log(num3 === num4);

let num5 = "5";
let num6 = 5;

console.log(num5 != num6); //false // checks value only. Doesn't check data type
console.log(num5 !== num6);